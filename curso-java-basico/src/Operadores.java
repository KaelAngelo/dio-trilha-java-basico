public class Operadores {
    

    public static void main(String[] args) {
        

        //Concatenação
        String nomeCompleto = "LINGUAGEM "+"JAVA";

        System.out.println(nomeCompleto);

        String concatenacao = "?";

        concatenacao = 1 + 1 +"1";

        System.out.println(concatenacao);

        concatenacao = 1+"1"+1+"1";

        System.out.println(concatenacao);

        //Operadores Unários

        int numero = 5;

        System.out.println(-numero);

        System.out.println(numero);


        //Incrementos

        int numeroC = 5;

        numeroC = numeroC + 1;

        System.out.println(numero);




        
    }
}
